<script>
    $(function(){
        let liststar = $(".rating");
        liststar.click(function(){
            let $this = $(this);
            let number = $this.attr('data-key');
            liststar.removeClass('rating_active');
            $.each(liststar, function(key,value){
                if(key +1 <= number){
                    $(this).addClass('rating_active');
                }
            });
            $(".number_rating").val(number);
        })
    })
</script>
